public abstract class Person {

  public String CNP;

  public String firstName;

  public String lastName;

  public String gendre;

  public String adress;

  public abstract void getCNP();

  public abstract void getFirstName();

  public abstract void getLastName();

  public abstract void getGendre();

  public abstract void getAdress();

  public abstract void setCNP();

  public abstract void setFirstName();

  public abstract void setLastName();

  public abstract void setGendre();

  public abstract void setAdress();

}